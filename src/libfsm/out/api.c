static int
rangeclass(unsigned char x, unsigned char y)
{
	int (*a[])(int c) = { isupper, islower, isdigit };
	size_t i;

	for (i = 0; i < sizeof a / sizeof *a; i++) {
		if (a[i](x) && a[i](y)) {
			return 1;
		}
	}

	return 0;
}

static unsigned int
indexof(const struct fsm *fsm, const struct fsm_state *state)
{
	struct fsm_state *s;
	unsigned int i;

	assert(fsm != NULL);
	assert(state != NULL);

	for (s = fsm->sl, i = 0; s != NULL; s = s->next, i++) {
		if (s == state) {
			return i;
		}
	}

	assert(!"unreached");
	return 0;
}

static int
escputc(int c, FILE *f)
{
	size_t i;

	const struct {
		int c;
		const char *s;
	} a[] = {
		{ '\\', "\\\\" },
		{ '\"', "\\\"" },
		{ '\'', "\\\'" },

		{ '\f', "\\f"  },
		{ '\n', "\\n"  },
		{ '\r', "\\r"  },
		{ '\t', "\\t"  },
		{ '\v', "\\v"  }
	};

	assert(c != FSM_EDGE_EPSILON);
	assert(f != NULL);

	for (i = 0; i < sizeof a / sizeof *a; i++) {
		if (a[i].c == c) {
			return fputs(a[i].s, f);
		}
	}

	if (!isprint((unsigned char) c)) {
		return fprintf(f, "\\%03o", (unsigned char) c);
	}

	return fprintf(f, "%c", c);
}

/* TODO: centralise */
static void
escputchar(int c, FILE *f)
{
	/* TODO: or if always_hex */

	if (c > SCHAR_MAX) {
		fprintf(f, "0x%02x", (unsigned char) c);
		return;
	}

	fprintf(f, "'");
	escputc(c, f);
	fprintf(f, "'");
}

void
fsm_out_api(const struct fsm *fsm, FILE *f)
{
	struct fsm_state *s, *start;
	struct bm *a; /* indexed by "to" state number */
	unsigned n;

	assert(fsm != NULL);
	assert(fsm->opt != NULL);
	assert(f != NULL);

/* TODO: leaf callback for opaques */

	if (fsm->opt->fragment) {
		fprintf(f, "\t");
	}
	fprintf(f, "/* Generated by libfsm */\n");
	fprintf(f, "\n");

	if (!fsm->opt->fragment) {
		fprintf(f, "#include LF_HEADER\n");
		fprintf(f, "\n");

		fprintf(f, "#include <stddef.h>\n");
		fprintf(f, "\n");

		fprintf(f, "#include <fsm/fsm.h>\n");
		fprintf(f, "\n");

		fprintf(f, "struct fsm *\n");
		fprintf(f, "%sfsm(const struct fsm_options *opt)\n",
			fsm->opt->prefix != NULL ? fsm->opt->prefix : "");

		fprintf(f, "{\n");
	}

	fprintf(f, "\tstruct fsm *fsm;\n");
	fprintf(f, "\tsize_t i;\n");
	fprintf(f, "\n");

	n = fsm_count(fsm, NULL, fsm_isany);
	fprintf(f, "\tstruct fsm_state *s[%u] = { 0 };\n", n);
	fprintf(f, "\n");

	fprintf(f, "\tfsm = fsm_new(opt);\n");
	fprintf(f, "\tif (fsm == NULL) {\n");
	fprintf(f, "\t\treturn NULL;\n");
	fprintf(f, "\t}\n");
	fprintf(f, "\n");

	fprintf(f, "\tfor (i = 0; i < %u; i++) {\n", n);
	fprintf(f, "\t\ts[i] = fsm_addstate(fsm);\n");
	fprintf(f, "\t\tif (s[i] == NULL) {\n");
	fprintf(f, "\t\t\tgoto error;\n");
	fprintf(f, "\t\t}\n");
	fprintf(f, "\t}\n");
	fprintf(f, "\n");

	a = malloc(n * sizeof *a);
	if (a == NULL) {
		/* XXX */
		return;
	}

	for (s = fsm->sl; s != NULL; s = s->next) {
		struct fsm_edge *e;
		struct fsm_state *st;
		struct set_iter it, jt;
		unsigned int from, to;
		unsigned int i;

		from = indexof(fsm, s);

		for (i = 0; i < n; i++) {
			bm_clear(&a[i]);
		}

		for (e = set_first(s->edges, &it); e != NULL; e = set_next(&it)) {
			for (st = set_first(e->sl, &jt); st != NULL; st = set_next(&jt)) {
				assert(st != NULL);

				to = indexof(fsm, st);

				if (e->symbol == FSM_EDGE_EPSILON) {
					fprintf(f, "\tif (!fsm_addedge_epsilon(fsm, s[%u], s[%u])) { goto error; }\n",
						from, to);
					continue;
				}

				bm_set(&a[indexof(fsm, st)], e->symbol);
			}
		}

		for (i = 0; i < n; i++) {
			int hi, lo;

			to = i;

			hi = -1;

			for (;;) {
				/* start of range */
				lo = bm_next(&a[to], hi, 1);
				if (lo > UCHAR_MAX) {
						break;
				}

				/* end of range */
				hi = bm_next(&a[to], lo, 0);

				if (lo == 0x00 && hi == UCHAR_MAX + 1) {
					fprintf(f, "\tif (!fsm_addedge_any(fsm, s[%u], s[%u]))",
						from, to);
					fprintf(f, " { goto error; }\n");
				} else if (lo == hi - 1) {
					fprintf(f, "\tif (!fsm_addedge_literal(fsm, s[%u], s[%u], ",
						from, to);
					escputchar(lo, f);
					fprintf(f, ")) { goto error; }\n");
				} else {
					fprintf(f, "\tfor (i = 0x%02x; i <= 0x%02x; i++) {",
						(unsigned int) lo, (unsigned int) hi - 1);
					if (rangeclass(lo, hi - 1)) {
						fprintf(f, " /* '%c' .. '%c' */", (unsigned char) lo, (unsigned char) hi - 1);
					}
					fprintf(f, "\n");
					fprintf(f, "\t\tif (!fsm_addedge_literal(fsm, s[%u], s[%u], i))",
						from, to);
					fprintf(f, " { goto error; }\n");
					fprintf(f, "\t}\n");
				}
			}
		}
	}

	free(a);

	fprintf(f, "\n");

	start = fsm_getstart(fsm);
	if (start == NULL) {
		return;
	}

	fprintf(f, "\tfsm_setstart(fsm, s[%u]);\n", indexof(fsm, start));

	for (s = fsm->sl; s != NULL; s = s->next) {
		if (fsm_isend(NULL, fsm, s)) {
			fprintf(f, "\tfsm_setend(fsm, s[%u], 1);\n", indexof(fsm, s));
		}
	}

	if (!fsm->opt->fragment) {
		fprintf(f, "\n");
		fprintf(f, "\treturn fsm;\n");

		fprintf(f, "\n");
		fprintf(f, "error:\n");
		fprintf(f, "\n");
		fprintf(f, "\tfsm_free(fsm);\n");
		fprintf(f, "\n");
		fprintf(f, "\treturn NULL;\n");

		fprintf(f, "}\n");
		fprintf(f, "\n");
	}
}

